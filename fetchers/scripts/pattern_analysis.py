# === 2. ODDS RANGE UPSET ===
print("\n=== ODDS RANGE (home odds) ===")
ranges = [(1.05,1.15),(1.15,1.25),(1.25,1.35),(1.35,1.50),(1.50,2.00),(2.00,2.50),(2.50,3.50),(3.50,6.00)]
for lo,hi in ranges:
    in_range = [m for m in all_matches if lo<=m[3]<hi]
    if not in_range: continue
    non_home = sum(1 for m in in_range if m[6]<=m[7])
    draws = sum(1 for m in in_range if m[6]==m[7])
    away_wins = sum(1 for m in in_range if m[6]<m[7])
    print(f"  odds {lo:.2f}-{hi:.2f}: {len(in_range)}场, 非主胜={non_home}({non_home/len(in_range)*100:.1f}%), 平局={draws}, 客胜={away_wins}")

# === 3. ULTRA-LOW ODDS ===
print("\n=== ULTRA-LOW ODDS (<1.20) ===")
ultra = [m for m in all_matches if m[3] < 1.20]
