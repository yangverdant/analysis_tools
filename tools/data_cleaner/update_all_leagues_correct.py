def parse_score(score_str):
    if not score_str:
        return None, None, None
    try:
        parts = score_str.split('-')
        home = int(parts[0])
        away = int(parts[1])
        result = 'H' if home > away else ('A' if home < away else 'D')
        return home, away, result
    except:
        return None, None, None


def normalize_team_name(name, team_mapping):
    """标准化球队名"""
    # 先尝试直接映射
    if name in team_mapping:
        return team_mapping[name]
    # 再尝试变体映射
    if name in TEAM_VARIANTS:
        return TEAM_VARIANTS[name]
    return name


