def update_teams():
    print("=" * 70)
    print("更新球队中文名 - 完整版")
    print("=" * 70)

    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    # 获取当前状态
    cursor.execute('SELECT COUNT(*) FROM teams')
    total = cursor.fetchone()[0]
    cursor.execute('SELECT COUNT(*) FROM teams WHERE name_cn IS NOT NULL AND name_cn != ""')
    has_cn = cursor.fetchone()[0]
    print(f"\n更新前: {total} 支球队, {has_cn} 个有中文名")

    # 批量更新
    updated = 0
    for team_name, name_cn in TEAM_CN_COMPLETE.items():
        cursor.execute('''
            UPDATE teams SET name_cn = ? WHERE name_en = ?
        ''', (name_cn, team_name))
        if cursor.rowcount > 0:
            updated += 1

    conn.commit()

    # 获取更新后状态
    cursor.execute('SELECT COUNT(*) FROM teams WHERE name_cn IS NOT NULL AND name_cn != ""')
    new_has_cn = cursor.fetchone()[0]

    print(f"\n更新结果:")
    print(f"  更新了 {updated} 个球队中文名")
    print(f"  更新后: {new_has_cn} 个有中文名 ({new_has_cn*100/total:.1f}%)")

    # 显示仍缺失的球队
    cursor.execute('SELECT name_en FROM teams WHERE name_cn IS NULL OR name_cn = "" ORDER BY name_en')
    missing = cursor.fetchall()
    if missing:
        print(f"\n仍有 {len(missing)} 个球队缺失中文名:")
        for t in missing[:30]:
            print(f"  {t[0]}")
        if len(missing) > 30:
            print(f"  ... 还有 {len(missing)-30} 个")

    conn.close()
    print("\n完成！")


if __name__ == '__main__':
    update_teams()
